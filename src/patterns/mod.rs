/// Get all unique categories.
pub fn categories() -> Vec<&'static str> {
    let mut cats: Vec<&'static str> = PATTERNS
        .iter()
        .map(|p| p.category)
        .collect();
    cats.sort_unstable();
    cats.dedup();
    cats
}

/// Get patterns for a specific category.
pub fn patterns_for_category(category: &str) -> Vec<&'static PatternDef> {
    PATTERNS
        .iter()
        .filter(|p| p.category == category)
        .collect()
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_pattern_count() {
        assert_eq!(PATTERNS.len(), 560);
    }

    #[test]
    fn test_categories_not_empty() {
        let cats = categories();
        assert!(!cats.is_empty());
    }

    #[test]
    fn test_patterns_for_category() {
        let cc = patterns_for_category("Credit Card Numbers");
        assert_eq!(cc.len(), 7);
    }

    #[test]
    fn test_all_regexes_compile() {
        use regex::RegexBuilder;
        for p in PATTERNS.iter() {
            let result = RegexBuilder::new(p.regex)
                .case_insensitive(p.case_insensitive)
                .build();
            assert!(
                result.is_ok(),
                "Failed to compile regex for {}/{}: {}",
                p.category,
                p.sub_category,
                result.unwrap_err()
            );
        }
    }
}
